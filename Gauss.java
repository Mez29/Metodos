    private double[] resolver(double[][] m, double[] r) {
        int n = this.getN();
        for(int i=0; i<n; i++){
            double d, c = 0;
            d = m[i][i];
            jTextArea1.append(Double.toString(1/d)+ " Fila"+(i+1) + "\n");
            for(int j =0; j<n; j++){
                m[i][j]= ((m[i][j])/d);
            }
            r[i] = ((r[i])/d);
            
            
            for (int j = 0; j<n; j++){
                for (int k=0; k< n; k++){
                    jTextArea1.append(Double.toString(m[j][k])+"\t");
                }
                jTextArea1.append("|\t"+ Double.toString(r[j])+"\n");
           }
            jTextArea1.append("\n\n");
            
         for (int x = 0; x<n; x++){
             if(i !=x){
                 c= m[x][i];
                 jTextArea1.append("-"+ Double.toString(c)+" Fila" + (i+1)+ "+ Fila" + (x+1)+ "\n");
                 for (int y=0; y <n; y++){
                     m[x][y] = m[x][y] -c * m [i][y];
                 }
                 r[x]= r[x]-c*r[i];
                 
                 for(int j= 0; j<n; j++){
                     for (int k=0; k<n; k++){
                         jTextArea1.append(Double.toString(m[j][k])+ "\t");
                     }
                     jTextArea1.append("|\t"+ Double.toString(r[j])+"\n");
                 }
                  jTextArea1.append("\n\n");
             }
             
         }
        }
        return r;
    }
